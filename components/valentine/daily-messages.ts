/**
 * Returns the message of the day based on the current date.
 * Uses day-of-year to rotate through messages, so a different
 * message appears each morning.
 */
export function getDailyMessage(): DailyMessage {
  const now = new Date()
  const startOfYear = new Date(now.getFullYear(), 0, 0)
  const diff = now.getTime() - startOfYear.getTime()
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24))
  const index = dayOfYear % dailyMessages.length
  return dailyMessages[index]
}
